class Cell:
    """A class for each cell of the grid"""
    def __init__(self, x, y, distance):
        self.x = x
        self.y = y
        self.distance = distance
    def is_over(self, pos):
        x = pos[0]
        y = pos[1]
        if x>self.x and y>self.y:
            if x<self.x+distance and y< self.y+distance:
                return True 
        return False


